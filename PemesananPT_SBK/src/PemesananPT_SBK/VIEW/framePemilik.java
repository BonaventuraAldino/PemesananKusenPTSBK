    private void jButtonTambahDesainKusenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonTambahDesainKusenActionPerformed
        // TODO add your handling code here:
        DesainKusen dk = new DesainKusen();

        int KD_DesainKusen_dt = 0;
        String Nama_Kusen_dt = jTextFieldNamaKusen.getText().toString();
        String Ukuran_dt = jTextFieldUkuran.getText().toString();
        String Bahan_dt = jTextFieldBahan.getText().toString();
        String harga_dt = jTextFieldHarga.getText().toString();

        int Harga_dt = Integer.parseInt(harga_dt);

        if (jTextFieldNamaKusen.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Harap masukkan nama kusen", "Peringatan", JOptionPane.WARNING_MESSAGE);
        } else if (jTextFieldUkuran.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Harap masukkan ukuran kusen", "Peringatan", JOptionPane.WARNING_MESSAGE);
        } else if (jTextFieldBahan.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Harap masukkan bahan kusen", "Peringatan", JOptionPane.WARNING_MESSAGE);
        } else if (jTextFieldHarga.getText().equals("0")) {
            JOptionPane.showMessageDialog(null, "Harap masukkan harga kusen", "Peringatan", JOptionPane.WARNING_MESSAGE);
        }
        else {
            dk.insert(KD_DesainKusen_dt, Nama_Kusen_dt, Ukuran_dt, Bahan_dt, Harga_dt);
            desainKusenJtable();
            labelKDDesainKusen.setVisible(false);
            txtKd_DesainKusen.setVisible(false);
            jTextFieldNamaKusen.setText("");
            jTextFieldUkuran.setText("");
            jTextFieldBahan.setText("");
            jTextFieldHarga.setText("0");
        }
    }//GEN-LAST:event_jButtonTambahDesainKusenActionPerformed

