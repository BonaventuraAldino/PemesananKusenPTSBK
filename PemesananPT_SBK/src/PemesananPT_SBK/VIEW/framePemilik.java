    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        Pesanan ps = new Pesanan();
        String kd_pemesanan_dt = txtKDpesanan.getText().toString();
        String kd_pelanggan_dt = txtKDpelanggan.getText().toString();
        String Nama_Pelanggan_dt = txtNamaPelanggan.getText().toString();
        Date tglpengambilan_dt = DateTglPengambilan.getDate();
        String Alamat_dt = txtAlamat.getText().toString();

        int KD_Pemesanan_dt = Integer.parseInt(kd_pemesanan_dt);
        int KD_Pelanggan_dt = Integer.parseInt(kd_pelanggan_dt);
        String Tgl_Pengambilan_dt = tahunHariTgl.format(tglpengambilan_dt);

        if (txtKDpesanan.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Harap pilih data pesanan", "Peringatan", JOptionPane.WARNING_MESSAGE);
        } else if (txtKDpelanggan.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Harap pilih data pesanan", "Peringatan", JOptionPane.WARNING_MESSAGE);
        } else if (txtNamaPelanggan.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Harap masukkan nama pelanggan", "Peringatan", JOptionPane.WARNING_MESSAGE);
        } else if (DateTglPengambilan.getDate().equals(null)){
            JOptionPane.showMessageDialog(null, "Harap masukkan tgl pengambilan", "Peringatan", JOptionPane.WARNING_MESSAGE);
        } else if (txtAlamat.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Harap masukkan alamat", "Peringatan", JOptionPane.WARNING_MESSAGE);
        }
        else {
            ps.ubah(KD_Pemesanan_dt, KD_Pelanggan_dt, Nama_Pelanggan_dt, Tgl_Pengambilan_dt, Alamat_dt);
            daftarPesananJtable();
            txtKDpesanan.setVisible(false);
            txtKDpelanggan.setVisible(false);
            txtNamaPelanggan.setText("");
            DateTglPengambilan.setDate(null);
            txtAlamat.setText("");
    }//GEN-LAST:event_jButton3ActionPerformed
    }
